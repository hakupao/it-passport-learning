#!/usr/bin/env node
// Generates scripts/quiz-ocr-verify-S101.workflow.mjs with the blind verification data
// embedded as a const (Workflow scripts have no fs access). Run once before invoking the
// Workflow tool with {scriptPath: 'scripts/quiz-ocr-verify-S101.workflow.mjs'}.
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SRC = path.join(ROOT, "evidence/phase5/stage_06_quiz_ocr_cleanup/ocr_cleanup_fixes_S101.json");
const OUT = path.join(ROOT, "scripts/quiz-ocr-verify-S101.workflow.mjs");

const data = JSON.parse(readFileSync(SRC, "utf-8"));
const fixes = data.fixes.map((f) => ({
  id: f.id, letter: f.letter, cls: f.cls, from: f.from, to: f.to, zh: f.zh, en: f.en, page: f.page,
}));
const fps = data.false_positives;

const DATA_JSON = JSON.stringify({ fixes, fps });

const script = `// AUTO-GENERATED by scripts/_gen-ocr-verify-workflow-S101.mjs — do not edit by hand.
// Blind independent re-derivation + adversarial verification of the S101 OCR-garble
// cleanup. Each derive agent sees ONLY the garbled JP + zh/en + source-page path (NOT the
// proposed fix) and re-derives the clean Japanese independently; the script then compares
// (punctuation/space-normalized) against the proposed fix and surfaces only real content
// disagreements. FP agents independently confirm the 7 false-positive groups.
export const meta = {
  name: 'ocr-cleanup-verify-S101',
  description: 'Blind re-derivation + FP-confirm for 58 OCR-garble choice fixes (D-140 cleanup)',
  phases: [
    { title: 'Derive', detail: 'blind independent re-derivation of clean JP per field' },
    { title: 'FP-confirm', detail: 'independent confirmation of false-positive groups' },
  ],
};

const DATA = ${DATA_JSON};

// normalize away punctuation-style + whitespace so only real content diffs remain
function norm(s) {
  return String(s == null ? '' : s)
    .replace(/[\\s\\u3000]+/g, '')
    .replace(/[,\\uff0c\\u3001]/g, '\\u3001')   // , ， 、  → 、
    .replace(/[.\\uff0e\\u3002]/g, '\\u3002');  // . ． 。  → 。
}

const DERIVE_SCHEMA = {
  type: 'object', additionalProperties: false,
  required: ['id', 'letter', 'clean_jp', 'garble_found', 'meaning_consistent', 'read_source_image', 'confidence', 'notes'],
  properties: {
    id: { type: 'string' }, letter: { type: 'string' },
    clean_jp: { type: 'string', description: 'corrected full Japanese choice text; only OCR garble removed, no paraphrase' },
    garble_found: { type: 'array', items: { type: 'string' }, description: 'garble fragments identified' },
    meaning_consistent: { type: 'boolean', description: 'clean_jp consistent with zh/en meaning' },
    read_source_image: { type: 'boolean' },
    confidence: { enum: ['high', 'medium', 'low'] },
    notes: { type: 'string' },
  },
};

const FP_SCHEMA = {
  type: 'object', additionalProperties: false,
  required: ['id', 'verdict', 'reasoning'],
  properties: {
    id: { type: 'string' },
    verdict: { enum: ['false_positive', 'real_garble'], description: 'false_positive = NOT garble, must NOT be changed' },
    reasoning: { type: 'string' },
  },
};

function derivePrompt(fx) {
  return [
    'You are auditing ONE multiple-choice answer choice from an IT Passport (ITパスポート) past exam.',
    'It was OCR-extracted from a Japanese source PDF and MAY contain OCR garble: misread characters,',
    'stray/duplicated punctuation, leaked page-number markers or section headers, digit/letter',
    'confusion (e.g. 0 misread for O), spurious mid-word characters, or trailing junk.',
    '',
    'Question: ' + fx.id + ', choice ' + fx.letter + '. (The correct-answer key is irrelevant to your task.)',
    '',
    'Current (possibly garbled) Japanese text, delimited by <<<>>>:',
    '<<<' + fx.from + '>>>',
    '',
    'Reference translations (made earlier by a translator who read the CLEAN source — authoritative for MEANING):',
    '  zh: ' + fx.zh,
    '  en: ' + fx.en,
    '',
    'Authoritative source page image: data/ip/exams/' + fx.page,
    'READ that image with the Read tool to confirm the exact original Japanese wording WHENEVER there is any',
    'ambiguity about trailing content, word boundaries, or which characters are garble. Prefer reading it.',
    '',
    'TASK: output clean_jp = the corrected full Japanese text for this choice. Remove ONLY genuine OCR garble;',
    'preserve every piece of legitimate content verbatim. Do NOT paraphrase, translate, summarize, or re-order.',
    'Keep the corpus punctuation style (ASCII ", " commas are acceptable). If already clean, return it unchanged.',
    'List the garble fragments you removed/fixed in garble_found. Set meaning_consistent if clean_jp matches the',
    'zh/en meaning. Be adversarial: do not over-delete real words, and do not leave real garble behind.',
  ].join('\\n');
}

function fpPrompt(fp) {
  return [
    'You are checking whether a deterministic OCR-garble detector produced a FALSE POSITIVE on an',
    'IT Passport past-exam question. The detector flagged the field(s) below, but they may actually be',
    'LEGITIMATE content that must NOT be altered.',
    '',
    'Question: ' + fp.id,
    'Flagged field(s): ' + fp.fields.join(', '),
    'Detector pattern: ' + fp.pattern,
    'Claimed reason it is a false positive: ' + fp.reason,
    '',
    'Independently judge: is the flagged text genuine OCR garble that should be fixed, or is it legitimate',
    'content (e.g. RAID0/RAID1 RAID levels, a real alphanumeric ESSID like A0B1C2D3E4, flow-diagram boxes',
    '[　　] / [Webサーバ], or IPA list/table full-width spacing) that must be left untouched?',
    'You may inspect data/ip/quiz/questions.json (raw choices_jp) and data/ip/quiz/translations/' + fp.id.replace(/-q\\d+$/, '') + '.json',
    'and the source page under data/ip/exams/pages/' + fp.id.replace(/-q\\d+$/, '') + '/ to decide.',
    'Return verdict=false_positive if it must NOT be changed, real_garble if the detector was right.',
  ].join('\\n');
}

// ── Phase 1: blind re-derivation ────────────────────────────────────────────────
phase('Derive');
log('Blind re-derivation of ' + DATA.fixes.length + ' choice fields…');
const derived = await parallel(DATA.fixes.map((fx) => () =>
  agent(derivePrompt(fx), { label: 'derive:' + fx.id + '-' + fx.letter, phase: 'Derive', schema: DERIVE_SCHEMA })
    .then((r) => {
      if (!r) return { id: fx.id, letter: fx.letter, dead: true, expected: fx.to };
      const match = norm(r.clean_jp) === norm(fx.to);
      return {
        id: fx.id, letter: fx.letter, cls: fx.cls,
        match, agent_clean: r.clean_jp, proposed: fx.to,
        meaning_consistent: r.meaning_consistent, read_source_image: r.read_source_image,
        confidence: r.confidence, garble_found: r.garble_found, notes: r.notes,
      };
    })
));

// ── Phase 2: false-positive confirmation ────────────────────────────────────────
phase('FP-confirm');
log('Confirming ' + DATA.fps.length + ' false-positive groups…');
const fpResults = await parallel(DATA.fps.map((fp) => () =>
  agent(fpPrompt(fp), { label: 'fp:' + fp.id, phase: 'FP-confirm', schema: FP_SCHEMA })
    .then((r) => r ? { ...r, fields: fp.fields } : { id: fp.id, dead: true })
));

const live = derived.filter((d) => d && !d.dead);
const disagreements = live.filter((d) => !d.match);
const lowconf = live.filter((d) => d.match && (d.confidence === 'low' || d.meaning_consistent === false));
const fpDisagree = fpResults.filter((f) => f && !f.dead && f.verdict !== 'false_positive');

return {
  summary: {
    derived_total: DATA.fixes.length,
    derived_live: live.length,
    derived_dead: derived.length - live.length,
    agree: live.filter((d) => d.match).length,
    disagree: disagreements.length,
    match_but_lowconf_or_inconsistent: lowconf.length,
    fp_total: DATA.fps.length,
    fp_confirmed: fpResults.filter((f) => f && !f.dead && f.verdict === 'false_positive').length,
    fp_disputed: fpDisagree.length,
  },
  disagreements,
  lowconf,
  fp_disputed: fpDisagree,
  fp_results: fpResults,
  all_derived: live,
};
`;

writeFileSync(OUT, script);
console.log(`✓ wrote workflow → ${path.relative(ROOT, OUT)} (${DATA_JSON.length} bytes data embedded)`);

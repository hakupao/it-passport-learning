function applyPatches() {
  let totalPatched = 0;
  let totalSkipped = 0;

  for (const [examId, qPatches] of Object.entries(patches)) {
    const filePath = path.join(BASE, `${examId}.json`);
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

    let patchedInFile = 0;

    for (const [qNumStr, choices] of Object.entries(qPatches)) {
      const qNum = parseInt(qNumStr, 10);
      const q = data.questions.find(q => q.question_number === qNum);

      if (!q) {
        console.warn(`  [WARN] ${examId} Q${qNum}: question not found`);
        totalSkipped++;
        continue;
      }

      const hasEmpty = Object.values(q.choices_jp).some(v => !v || v.trim() === '');
      if (!hasEmpty) {
        console.log(`  [SKIP] ${examId} Q${qNum}: already has choices`);
        totalSkipped++;
        continue;
      }

      q.choices_jp = { ...q.choices_jp, ...choices };
      patchedInFile++;
      totalPatched++;
      console.log(`  [OK]   ${examId} Q${qNum}: patched`);
    }

    fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
    console.log(`=> ${examId}: ${patchedInFile} questions patched`);
  }

  console.log(`\nTotal patched: ${totalPatched}, skipped: ${totalSkipped}`);
}

applyPatches();
